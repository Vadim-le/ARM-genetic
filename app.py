def clean_sequence(sequence):
    """Удаляет все символы, кроме A, C, T, G из последовательности."""
    return ''.join(filter(lambda x: x in 'ACTG', sequence.upper()))

def find_repeating_sequences(sequence, min_length, max_length, max_distance=100):
    repeats = {}

    for length in range(min_length, max_length + 1):
        seen = {}
        for i in range(len(sequence) - length + 1):
            substring = sequence[i:i + length]
            if substring in seen:
                if substring not in repeats:
                    repeats[substring] = [seen[substring]]
                # Проверяем расстояние между текущей и предыдущей позицией
                if i - repeats[substring][-1] <= max_distance:
                    repeats[substring].append(i)
            else:
                seen[substring] = i

    # Удаляем повторения, которые встречаются только один раз или не соответствуют критерию расстояния
    repeats = {k: v for k, v in repeats.items() if len(v) > 1 and all(v[i] - v[i - 1] <= max_distance for i in range(1, len(v)))}

    return repeats

def find_spacers_with_indices(sequence, repeats, min_spacer_length=20, max_spacer_length=60, min_distance_between_spacers=10):
    spacers_info = []
    last_spacer_end = -1  # Переменная для отслеживания конца последнего добавленного спейсера

    for repeat, positions in repeats.items():
        for i in range(1, len(positions)):
            repeat_start_1 = positions[i-1]
            repeat_end_1 = repeat_start_1 + len(repeat)
            repeat_start_2 = positions[i]
            repeat_end_2 = repeat_start_2 + len(repeat)

            # Извлечение спейсера между повторами
            spacer_start = repeat_end_1
            spacer_end = repeat_start_2
            spacer = sequence[spacer_start:spacer_end]

            if min_spacer_length <= len(spacer) <= max_spacer_length:
                # Проверяем расстояние между текущим спейсером и последним добавленным
                if spacer_start - last_spacer_end >= min_distance_between_spacers:
                    is_known = spacer in known_spacers

                    # Определяем контекстный участок, включая повтор, спейсер и 100 символов слева и справа
                    context_start = max(0, repeat_start_1 - 100)
                    context_end = min(len(sequence), repeat_end_2 + 100)
                    full_context = sequence[context_start:context_end]

                    spacers_info.append({
                        'repeat': repeat,
                        'repeat_positions': [(repeat_start_1, repeat_end_1), (repeat_start_2, repeat_end_2)],
                        'spacer': spacer,
                        'spacer_positions': (spacer_start, spacer_end),
                        'is_known': is_known,
                        'full_context': full_context
                    })
                    last_spacer_end = spacer_end  # Обновляем конец последнего добавленного спейсера

    return spacers_info

@app.route('/find_repeats', methods=['POST'])
def find_repeats():
    # Проверяем, есть ли файл в запросе
    if 'file' not in request.files:
        return jsonify({'error': 'Файл не загружен'}), 400

    file = request.files['file']
    if file.filename == '':
        return jsonify({'error': 'Имя файла пустое'}), 400

    # Чтение содержимого файла
    sequence = file.read().decode('utf-8')

    # Получение параметров из запроса
    min_length = int(request.form.get('min_length', 23))
    max_length = int(request.form.get('max_length', 44))

    # Очистка последовательности
    cleaned_sequence = clean_sequence(sequence)

    # Поиск повторяющихся последовательностей
    repeats = find_repeating_sequences(cleaned_sequence, min_length, max_length)

    # Поиск спейсеров и их индексов
    spacers_info = find_spacers_with_indices(cleaned_sequence, repeats)

    # Вывод информации о повторяющихся последовательностях и спейсерах в ��онсоль
    for info in spacers_info:
        print(f"Повтор: {info['repeat']}, Позиции повторов: {info['repeat_positions']}, "
              f"Спейсер: {info['spacer']}, Позиции спейсера: {info['spacer_positions']}, "
              f"Известный: {info['is_known']}, "
              f"Полный контекст: {info['full_context']}")

    # Возвращаем количество найденных повторов и возможных спейсеров
    return jsonify({
        'spacers_info': spacers_info,
        'num_repeats': len(repeats),
        'num_spacers': len(spacers_info)
    })

